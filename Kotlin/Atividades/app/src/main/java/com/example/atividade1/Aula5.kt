fun main() {
    println("Digite o seu nome ")
    var n1 = readLine()!!.toInt()
    println("Digte o segundo número: ")
    var n2 = readLine()!!.toInt()
    println("A soma é: " + (n1 + n2))
    println("A subtração é: " + (n1 - n2))
    println("a divisão é: " + (n1 / n2))
    println("a multiplicação é: " + (n1 * n2))
}

